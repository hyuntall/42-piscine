#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	print_num(int nb)
{
	if (nb / 10 == 0)
	{
		ft_putchar('0' + nb);
		return ;
	}
	print_num(nb / 10);
	ft_putchar('0' + nb % 10);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	print_num(nb);
}
